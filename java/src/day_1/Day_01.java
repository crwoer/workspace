package day_1;
// 常量、变量、数据类型
public class Day_01 {
//  常量
    public static void main(String[] args){
//        输出整数常量
        System.out.println(123);
//        输出小数常量
        System.out.println(0.123);
//        输出字符常量
        System.out.println('a');
//        输出布尔常量
        System.out.println(true);
//        输出字符串常量
        System.out.println("你好java");
    }
}
