package day_1;

public class Day_02_variable {
//    变量
    public static void main(String[] args){
//        定义字节型变量
        byte b = 100;
        System.out.println(b);
//        定义短整型变量
        short s = 100 ;
        System.out.println(s);
//        定义整型变量
        int i = 123456;
        System.out.println(i);
//        定义长整型变量
        long l = 1234345254234L;
        System.out.println(l);
//        定义单精度浮点型变量
        float f = 5.5F;
        System.out.println(f);
//        定义双精度浮点型变量
        double d = 8.5;
        System.out.println(d);
//        定义布尔型变量
        boolean bool = false;
        System.out.println(bool);
//        定义字符型变量

    }
}
